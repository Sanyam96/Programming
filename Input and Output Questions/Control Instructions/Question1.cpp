#include <bits/stdc++.h>
using namespace std;

int main() {

	int i = 4;
	switch(i) {
		
		default : 
			cout << "In Default" << endl;

		case 1 :
			cout << "Case1" << endl;
			break;

		case 2 :
			cout << "Case2" << endl;
			break;

		case 3 : 
			cout << "Case3" << endl;
			break;
	}
    return 0;
}


